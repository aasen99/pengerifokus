function klarnaSlug(partner: string, index: number): string {
  const base = partner
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
  return `klarna-${base || index}`;
}

